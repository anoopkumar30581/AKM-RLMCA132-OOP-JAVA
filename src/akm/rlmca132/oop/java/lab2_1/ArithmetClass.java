public class ArithmetClass {
    public static void main ( String args [ ] ){
        float x = 23.5f, y = 7.3f;
        System.out.println ("x =" + x);
        System.out.println ("y =" + y);
        System.out.println ("x + y =" + ( x + y) ) ;
        System.out.println ("x - y =" + (x - y) ) ;
        System.out.println (" x * y =" +( x* y) );
        System.out.println (" x / y =" + ( x / y ) );
        System.out.println (" x % y =" + ( x % y ) );
    }
}
